"""
Program the turtle to draw a stairs with 4 steps.
 each step is 10 high and has a width of 50.
You can stop at 4 steps.
"""

import turtle

turtle.left(90)
turtle.forward(10)
turtle.right(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(10)
turtle.right(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(10)
turtle.right(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(10)
turtle.right(90)
turtle.forward(50)
turtle.right(90)
turtle.forward(40)
turtle.right(90)
turtle.forward(200)

turtle.exitonclick()
